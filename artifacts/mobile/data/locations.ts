/**
 * Returns the neighborhood list for a given city label.
 * Returns an empty array if the city has no configured neighborhoods.
 */
export function getNeighborhoods(city: string): string[] {
  return TEXAS_LOCATIONS.find((l) => l.city === city)?.neighborhoods ?? [];
}

/**
 * Case-insensitive substring filter for neighborhood search.
 */
export function filterNeighborhoods(neighborhoods: string[], query: string): string[] {
  if (!query.trim()) return neighborhoods;
  const q = query.toLowerCase();
  return neighborhoods.filter((n) => n.toLowerCase().includes(q));
}
